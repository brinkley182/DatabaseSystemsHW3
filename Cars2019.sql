DROP TABLE INVENTORY CASCADE CONSTRAINTS;
DROP TABLE CAR CASCADE CONSTRAINTS;
DROP TABLE DEALER CASCADE CONSTRAINTS;
CREATE TABLE CAR
(
	ID		CHAR(6)		PRIMARY KEY,
	Company		VARCHAR2(12),
	Model		VARCHAR2(16),
	Year		NUMBER(4),
	Price		NUMBER(8,2)	CHECK (Price >= 0)
);
CREATE TABLE DEALER
(
	ID		CHAR(4)		PRIMARY KEY,
	Name		VARCHAR2(40),
	City		VARCHAR2(20),
	State		CHAR(2),
	Zip		CHAR(5)
);
CREATE TABLE INVENTORY
(
	CarID		CHAR(6)		REFERENCES CAR(ID),
	DealerID	CHAR(4)		REFERENCES DEALER(ID),
	Quantity	NUMBER(3)	CHECK (Quantity >= 1),
	PRIMARY KEY (CarID, DealerID)
);
